    private void signupBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signupBtnActionPerformed
        String name = nameField.getText();
        String email = emailField.getText();
        String password = String.valueOf(passwordField.getPassword());
        String confirm_password = String.valueOf(confirmPasswordField.getPassword());
        String age = ageField.getText();
        String login_as = signupAsField.getSelectedItem().toString();

        // safety checks for entering the data in the database
        if (name.equals(null) || name.equals("Enter your name")) {
            JOptionPane.showMessageDialog(null, "Enter a valid name!", "Invalid Input", 2);
        } else if (email.equals(null) || email.equals("Enter your email ID")) {
            JOptionPane.showMessageDialog(null, "Enter a valid email ID!", "Invalid Input", 2);
        } else if (password.equals(null) || password.equals("Enter your password")) {
            JOptionPane.showMessageDialog(null, "Enter a valid password!", "Invalid Input", 2);
        } else if (confirm_password.equals(null) || confirm_password.equals("Confirm your password")) {
            JOptionPane.showMessageDialog(null, "Confirm your password!", "Invalid Input", 2);
        } else if (!confirm_password.equals(password)) {
            JOptionPane.showMessageDialog(null, "Confirm Password is not same as your password!", "Invalid Input", 2);
        } else if (age.equals(null) || age.equals("Enter your age")) {
            JOptionPane.showMessageDialog(null, "Enter a valid age!", "Invalid Input", 2);
        } else if (Integer.parseInt(age) < 0 || Integer.parseInt(age) > 100) {
            JOptionPane.showMessageDialog(null, "Enter a valid age!", "Invalid Input", 2);
        } else {
            if (DB.addSignupData(name, email, password, confirm_password, Integer.parseInt(age), login_as)) { // if true is returned
                openWindow(login_as);
                this.dispose();
            }
        }
    }//GEN-LAST:event_signupBtnActionPerformed


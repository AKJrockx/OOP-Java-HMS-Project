    public static void openWindow(String WindowName) {
        if (WindowName.equals("Admin")) {
            AdminHomePage window = new AdminHomePage();
            window.setVisible(true);
            window.pack();
            window.setLocationRelativeTo(null);
        } else if (WindowName.equals("User")) {
            UserPage window = new UserPage();
            window.setVisible(true);
            window.pack();
            window.setLocationRelativeTo(null);
        } else if (WindowName.equals("Staff")) {
            StaffPage window = new StaffPage();
            window.setVisible(true);
            window.pack();
            window.setLocationRelativeTo(null);
        } else if (WindowName.equals("Developer")) {
            DeveloperPage window = new DeveloperPage();
            window.setVisible(true);
            window.pack();
            window.setLocationRelativeTo(null);
        } else {
            System.out.println("no such window found");
        }
    }

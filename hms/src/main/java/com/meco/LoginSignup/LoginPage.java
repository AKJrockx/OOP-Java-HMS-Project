    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        PreparedStatement stmt;
        ResultSet rs;

        String username = emailField.getText().trim();
        String password = String.valueOf(passwordField.getPassword()).trim();
        String loginAs = String.valueOf(loginAsComboBox.getSelectedItem());

        String query = "SELECT email, password, login_as FROM userdata WHERE email = ? AND password = ? AND login_as = ?";
        try {
            // stmt = DB.getConnection().createStatement();
            stmt = DB.getConnection().prepareStatement(query);
            stmt.setString(1, username);
            stmt.setString(2, password);
            stmt.setString(3, loginAs);
            rs = stmt.executeQuery();
            
            String storedUsername = "";
            String storedPassword = "";
            String storedLoginAs = "";
            while(rs.next()) {
                storedUsername = rs.getString("email").trim();
                storedPassword = rs.getString("password").trim();
                storedLoginAs = rs.getString("login_as").trim();
                System.out.println(storedUsername + " " + storedPassword + " " + storedLoginAs);
            }
            if (username.equals(storedUsername) && password.equals(storedPassword) && loginAs.equals(storedLoginAs)){
                if (loginAs == "Admin") {
                    AdminHomePage page = new AdminHomePage();
                    page.setVisible(true);
                    page.pack();
                    page.setLocationRelativeTo(null);
                    this.dispose();
                } else if (loginAs == "User") {
                    if (IS_USER_ROOM_BOOKED) {
                        UserSignedInPage page = new UserSignedInPage();
                        page.setVisible(true);
                        page.pack();
                        page.setLocationRelativeTo(null);
                        this.dispose();
                    } else {
                        UserPage page = new UserPage();
                        page.setVisible(true);
                        page.pack();
                        page.setLocationRelativeTo(null);
                        this.dispose();
                    }
                } else if (loginAs == "Staff") {
                    StaffPage page = new StaffPage();
                    page.setVisible(true);
                    page.pack();
                    page.setLocationRelativeTo(null);
                    this.dispose();
                } else if (loginAs == "Developer") {
                    DeveloperPage page = new DeveloperPage();
                    page.setVisible(true);
                    page.pack();
                    page.setLocationRelativeTo(null);
                    this.dispose();
                }
            } else JOptionPane.showMessageDialog(null, "Invalid Credentials", "Login Failed", 2);           
        } catch (SQLException e) {System.out.println(e.getMessage());}
    }//GEN-LAST:event_loginBtnActionPerformed


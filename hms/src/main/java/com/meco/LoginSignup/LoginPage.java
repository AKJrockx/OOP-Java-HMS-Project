    private void emailFieldFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_emailFieldFocusGained
        // clear the text field on focus if the text is "username"
        if (emailField.getText().trim().equals("Enter your username")) {
            emailField.setText("");
        }
    }//GEN-LAST:event_emailFieldFocusGained

    private void emailFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_emailFieldFocusLost
        // re-enter the text "Enter username" if focus is lost
        if (emailField.getText().equals("") || emailField.getText().equals("Enter your username")) {
            emailField.setText("Enter your username");
        }
    }//GEN-LAST:event_emailFieldFocusLost

    private void passwordFieldFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_passwordFieldFocusGained
        // clear the text field on focus if the text is "Enter password"
        if (String.valueOf(passwordField.getPassword()).equals("Enter your password")) {
            passwordField.setText("");
        }
    }//GEN-LAST:event_passwordFieldFocusGained

    private void passwordFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_passwordFieldFocusLost
        // re-enter the text "Enter username" if focus is lost
        if (String.valueOf(passwordField.getPassword()).equals("")
                || String.valueOf(passwordField.getPassword()).equals("Enter your password")) {
            passwordField.setText("Enter your password");
        }
    }//GEN-LAST:event_passwordFieldFocusLost

